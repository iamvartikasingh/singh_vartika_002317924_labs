    private void btnViewTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewTActionPerformed
        // TODO add your handling code here:
        int selectedIndex =tbVitals.getSelectedRow();
        if(selectedIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tbVitals.getModel();
        VitalSigns selectedVitals =(VitalSigns) model.getValueAt(selectedIndex,0);
        
        if(selectedVitals!=null){
            txtTemprature.setText(Double.toString(selectedVitals.getTemprature()));
            txtBlood.setText(Double.toString(selectedVitals.getBloodPressure()));
            txtPulse.setText(Integer.toString(selectedVitals.getPulse()));
            txtDate.setText(selectedVitals.getDate());
            
        }
    }//GEN-LAST:event_btnViewTActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
         int selectedIndex =tbVitals.getSelectedRow();
        if(selectedIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tbVitals.getModel();
        VitalSigns selectedVitals =(VitalSigns) model.getValueAt(selectedIndex,0);
        
        if (selectedVitals!=null){
            vitalSignsHistory.removeVitals(selectedVitals);
            JOptionPane.showMessageDialog(this,"Vital Signs Deleted!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }
        populateTable();
    }//GEN-LAST:event_btnDelActionPerformed


