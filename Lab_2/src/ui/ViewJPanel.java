private void populateTable(){
     DefaultTableModel model =(DefaultTableModel) tbVitals.getModel();
     model.setRowCount(0);
     
     for(VitalSigns vs: vitalSignsHistory.getHistory()){
         Object[] row = new Object[4];
         row[0] = vs;
         row[1] = vs.getTemprature();
         row[2] = vs.getBloodPressure();
         row[3] = vs.getPulse();
         
         model.addRow(row);
     }
}


